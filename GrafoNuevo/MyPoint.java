package xxGrafoNuevo;

public class MyPoint {
	private double id,x,y;

	public MyPoint(double id, double x, double y) {
		super();
		this.id = id;
		this.x = x;
		this.y = y;
	}

	public double getId() {
		return id;
	}

	public void setId(double id) {
		this.id = id;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

}
